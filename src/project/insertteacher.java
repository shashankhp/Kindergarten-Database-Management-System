    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
         
         int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
                String name = txtname.getText();
                String num = txtno.getText();
                String addr = txtaddr.getText();
        
        if(name.isEmpty() || addr.isEmpty() )
                    JOptionPane.showMessageDialog(this,"Fill all the details");

                else{
         try {

                int val = 0;
                
                for (int i = 0; i < num.length(); i++) { 
                    if (Character.isDigit(num.charAt(i))) { 
                        val = 1; 
                    } 
                    else { 
                        val = 0; 
                    }   
                } 
                
          
                if (num.length()!=10 || val == 0)
                {
                     JOptionPane.showMessageDialog(this,"Invalid Number");
                }
                
                else{
                
                    Class.forName("com.mysql.jdbc.Driver");
                    con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/kindergarten?useSSL=false","root","Shashank-ssk");

                    insert = con1.prepareStatement("update teacher set tname = ?, tphno =?, taddr = ? where tid=?");
                    insert.setString(1,name);
                    insert.setString(2,num);
                    insert.setString(3,addr);
                    insert.setInt(4,id);

                    insert.executeUpdate();

                    JOptionPane.showMessageDialog(this,"Record Updated");
                    table_update();

                    txtname.setText("");
                    txtno.setText("");
                    txtaddr.setText("");
                    txtname.requestFocus();
                }
        }
                 
        catch (ClassNotFoundException ex) {
                    Logger.getLogger(newchild.class.getName()).log(Level.SEVERE, null, ex);
            } 
            catch (SQLException ex) {
                    Logger.getLogger(newchild.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

