    private void table_update(){
      
       int c;
          try {
                    Class.forName("com.mysql.jdbc.Driver");
                    con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/kindergarten?useSSL=false","root","Shashank-ssk");
                    
                    insert = con1.prepareStatement("select * from teacher");
                    ResultSet rs = insert.executeQuery();
                    
                    ResultSetMetaData Rss = (ResultSetMetaData) rs.getMetaData();
                    c = Rss.getColumnCount();
                    
                    DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
                    Df.setRowCount(0);
                    
                    while(rs.next()) {
                    
                        Vector v = new Vector();
                    
                        for(int a = 1; a<=c; a++){
                            
                            v.add(rs.getString("tid"));
                            v.add(rs.getString("tname"));
                            v.add(rs.getString("tphno"));
                            v.add(rs.getString("taddr"));
                                
                        }
                    
                        Df.addRow(v);
  
                    } 
          }
            catch (ClassNotFoundException ex) {
                    Logger.getLogger(newchild.class.getName()).log(Level.SEVERE, null, ex);
            } 
            catch (SQLException ex) {
                    Logger.getLogger(newchild.class.getName()).log(Level.SEVERE, null, ex);
            }
      
      }
    
